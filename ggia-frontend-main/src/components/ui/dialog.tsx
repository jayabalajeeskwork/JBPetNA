  React.ElementRef<typeof DialogPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof DialogPrimitive.Content>
>(({ className, children, ...props }, ref) => (
  <DialogPortal>
    <DialogOverlay />
    <DialogPrimitive.Content
      ref={ref}
      className={cn(
        "fixed left-1/2 top-1/2 z-50 grid w-full max-w-[640px] -translate-x-1/2 -translate-y-1/2 gap-4 rounded-[24px] border border-[#E5E7EB] bg-white p-6 shadow-[0_18px_45px_rgba(15,23,42,0.25)] sm:p-8",
        "data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95",
        "focus-visible:outline-none",
        className
      )}
      {...props}
    >
      {children}
      {/* <DialogPrimitive.Close
        className={cn(
          "absolute right-4 top-4 inline-flex size-8 items-center justify-center rounded-full text-[#4B5563] transition-colors hover:bg-[#F3F4F6] focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[#03838C]/60"
        )}
        aria-label="Close"
      >
        <X className="size-4" />
      </DialogPrimitive.Close> */}
    </DialogPrimitive.Content>
  </DialogPortal>
))
DialogContent.displayName = DialogPrimitive.Content.displayName

const DialogHeader = ({ className, ...props }: React.HTMLAttributes<HTMLDivElement>) => (
  <div className={cn("flex flex-col space-y-2 text-left", className)} {...props} />
)
DialogHeader.displayName = "DialogHeader"

